export type PacketDirection = "CLIENT" | "SERVER" | "RELAY";

export function packetNameFor(direction: PacketDirection, header: number | null): string | null {
  if (header === null || !Number.isFinite(header)) return null;
  const key = String(header);
  if (direction === "CLIENT") return packetNames.outgoing[key as keyof typeof packetNames.outgoing] ?? "UNKNOWN_HEADER";
  if (direction === "SERVER") return packetNames.incoming[key as keyof typeof packetNames.incoming] ?? "UNKNOWN_HEADER";
  return null;
}
